package com.class2;

public class StringConcatenation {

	public static void main(String[] args) {

		String name = "John";

		// My name is John;

		System.out.println("My name is " + name);

		String day = "Sunday";
		// Sunday is a coding day

		System.out.println(day + " is a coding day");

		int num1 = 12;
		int num2 = 13;

		// The value of num1 is______ and the value of num2 is______

		System.out.println("The value of num1 is " + num1 + " and value of num2 is " + num2);

		String name1 = "John";
		String lastName2 = "Smith";
		char grade = 'B';
		String city = "Fairfax";
		String state = "VA";
		long phoneNumber = 201345675;

		System.out.println("My name is " + name1 + " and my last name is " + lastName2);
		System.out.println("I am " + grade + " student");
		System.out.println("I live in city of " + city + " in a state of " + state);
		System.out.println("My Phone number is " + phoneNumber);

	}

}
